/**
 * An enumeration representing the types of files.
 * @enum {string}
 */
export enum FileType {
  /**
   * Indicates an audio file.
   */
  AUDIO = 'AUDIO',
  /**
   * Indicates an image file.
   */
  IMAGE = 'IMAGE',
  /**
   * Indicates a text file.
   */
  TEXT = 'TEXT',
  /**
   * Indicates an unknown file type.
   */
  UNKNOWN = 'UNKNOWN',
  /**
   * Indicates a video file.
   */
  VIDEO = 'VIDEO',
}
